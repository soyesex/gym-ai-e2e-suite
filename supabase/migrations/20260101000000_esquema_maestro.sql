CREATE OR REPLACE FUNCTION "public"."match_exercises"("query_embedding" "public"."vector", "match_threshold" double precision, "match_count" integer) RETURNS TABLE("id" "uuid", "name" "text", "name_es" "text", "description" "text", "description_es" "text", "primary_muscle" "public"."muscle_group", "equipment" "public"."equipment_required", "difficulty" "public"."difficulty_level", "similarity" double precision)
    LANGUAGE "plpgsql"
    AS $$
BEGIN
  RETURN QUERY
  SELECT
    e.id, e.name, e.name_es, e.description, e.description_es,
    e.primary_muscle, e.equipment, e.difficulty,
    1 - (e.embedding <=> query_embedding) AS similarity
  FROM public.exercises e
  WHERE e.embedding IS NOT NULL
    AND 1 - (e.embedding <=> query_embedding) > match_threshold
  ORDER BY e.embedding <=> query_embedding
  LIMIT match_count;
END;
$$;


