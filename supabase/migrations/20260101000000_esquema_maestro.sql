CREATE OR REPLACE FUNCTION "public"."handle_new_user"() RETURNS "trigger"
    LANGUAGE "plpgsql" SECURITY DEFINER
    SET "search_path" TO 'public'
    AS $$
BEGIN
    INSERT INTO public.profiles (id, username)
    VALUES (
        NEW.id,
        NULLIF(TRIM(NEW.raw_user_meta_data ->> 'username'), '')
    )
    ON CONFLICT (id) DO NOTHING;

    RETURN NEW;
END;
$$;


ALTER FUNCTION "public"."handle_new_user"() OWNER TO "postgres";


CREATE OR REPLACE FUNCTION "public"."handle_xp_gamification"() RETURNS "trigger"
    LANGUAGE "plpgsql" SECURITY DEFINER
    AS $$
DECLARE
    xp_gained INTEGER;
    minutes_trained INTEGER;
    new_xp INTEGER;
    new_level INTEGER;
    new_level_label experience_level;
BEGIN
    -- Only execute when workout transitions to 'completed'
    IF NEW.status = 'completed' AND OLD.status != 'completed' AND NEW.ended_at IS NOT NULL THEN

        -- Calculate training duration in minutes
        minutes_trained := EXTRACT(EPOCH FROM (NEW.ended_at - NEW.started_at)) / 60;
        IF minutes_trained < 0 OR minutes_trained IS NULL THEN
            minutes_trained := 0;
        END IF;

        -- XP formula: 50 base + 5 per minute
        xp_gained := 50 + (minutes_trained * 5);

        -- Get user's current XP and compute new total
        SELECT current_xp + xp_gained INTO new_xp
        FROM public.profiles
        WHERE id = NEW.user_id;

        -- Compute level from thresholds (10-level table):
        -- Lv1: 0 | Lv2: 100 | Lv3: 250 | Lv4: 450 | Lv5: 750
        -- Lv6: 1150 | Lv7: 1650 | Lv8: 2300 | Lv9: 3100 | Lv10: 4100
        new_level := CASE
            WHEN new_xp >= 4100 THEN 10
            WHEN new_xp >= 3100 THEN 9
            WHEN new_xp >= 2300 THEN 8
            WHEN new_xp >= 1650 THEN 7
            WHEN new_xp >= 1150 THEN 6
            WHEN new_xp >= 750  THEN 5
            WHEN new_xp >= 450  THEN 4
            WHEN new_xp >= 250  THEN 3
            WHEN new_xp >= 100  THEN 2
            ELSE 1
        END;

        -- Map numeric level → experience_level enum for the profile level column
        new_level_label := CASE
            WHEN new_level <= 1  THEN 'sedentary'::experience_level
            WHEN new_level <= 3  THEN 'beginner'::experience_level
            WHEN new_level <= 5  THEN 'intermediate'::experience_level
            WHEN new_level <= 7  THEN 'advanced'::experience_level
            ELSE                      'elite'::experience_level
        END;

        -- Debug logging (can be removed later)
        RAISE LOG '[handle_xp_gamification] user=% xp_gained=% new_xp=% new_level=% label=%',
            NEW.user_id, xp_gained, new_xp, new_level, new_level_label;

        -- Update the user's gamification stats
        UPDATE public.profiles
        SET
            current_xp    = new_xp,
            current_level = new_level,
            level         = new_level_label,
            updated_at    = NOW()
        WHERE id = NEW.user_id;

    END IF;
    RETURN NEW;
END;
$$;


ALTER FUNCTION "public"."handle_xp_gamification"() OWNER TO "postgres";


CREATE OR REPLACE FUNCTION "public"."is_username_available"("search_username" "text") RETURNS boolean
    LANGUAGE "plpgsql" SECURITY DEFINER
